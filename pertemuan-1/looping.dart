void main() {
  // while loop

  var flag = 1;
  // inisialisasi diluar for
  // increment decrement
  while (flag <= 10); {
    print('iterasi ke - ' + flag.toString()); // iterasi = pengulangan
    flag++;
  }

  // for-loop
  print('-----For Loop-----');

  for(var angka = 1; angka < 10; angka++) {
    print('iterasi ke - ' + angka.toString());
    // ketiganya di dalem print

  }
}